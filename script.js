let currentQuestions = [];
let currentQuestionIndex = 0;
let correctAnswers = 0;
let incorrectAnswers = 0;

function startQuiz(difficulty) {
    document.getElementById('menu').style.display = 'none';
    document.getElementById('quiz-container').style.display = 'block';
    currentQuestions = questions[difficulty];
    currentQuestionIndex = 0;
    correctAnswers = 0;
    incorrectAnswers = 0;
    loadQuestion();
}

function loadQuestion() {
    const question = currentQuestions[currentQuestionIndex];
    document.getElementById('question').textContent = question.question;
    
    const answersDiv = document.getElementById('answers');
    answersDiv.innerHTML = ''; // Limpar respostas anteriores
    question.options.forEach(option => {
        const answerDiv = document.createElement('div');
        answerDiv.textContent = option;
        answerDiv.onclick = () => checkAnswer(option, question);
        answersDiv.appendChild(answerDiv);
    });

    // Esconder feedback e continuar bot√£o
    document.getElementById('feedback').style.display = 'none';
    document.getElementById('continue-btn').style.display = 'none';
}

function checkAnswer(selected, question) {
    const feedback = document.getElementById('feedback');
    if (selected === question.correct) {
        correctAnswers++;
        feedback.innerHTML = `<p>Acertou! üéâ</p><p>${question.explanation}</p>`;
        showConfetti();
    } else {
        incorrectAnswers++;
        feedback.innerHTML = `<p class="error">Errou! ‚ùå</p><p>${question.explanation}</p>`;
    }

    // Mostrar explica√ß√£o e bot√£o "Continuar"
    document.getElementById('feedback').style.display = 'block';
    document.getElementById('continue-btn').style.display = 'inline-block';
}

function showConfetti() {
    for (let i = 0; i < 30; i++) {
        const confetti = document.createElement('div');
        confetti.classList.add('confetti');
        confetti.style.left = Math.random() * window.innerWidth + 'px';
        confetti.style.animationDuration = (Math.random() * 1 + 1) + 's';
        document.body.appendChild(confetti);
        setTimeout(() => confetti.remove(), 2000); // Remover confete ap√≥s anima√ß√£o
    }
}

function continueQuiz() {
    if (currentQuestionIndex < currentQuestions.length - 1) {
        currentQuestionIndex++;
        loadQuestion();
    } else {
        showResults();
    }
}

function showResults() {
    document.getElementById('quiz-container').style.display = 'none';
    document.getElementById('result').style.display = 'block';
    document.getElementById('acertos').textContent = `Acertos: ${correctAnswers}`;
    document.getElementById('erros').textContent = `Erros: ${incorrectAnswers}`;
}

function resetQuiz() {
    document.getElementById('result').style.display = 'none';
    document.getElementById('menu').style.display = 'block';
}

function backToMenu() {
    document.getElementById('result').style.display = 'none';
    document.getElementById('menu').style.display = 'block';
}